import type { AnalysisResult, SelfHealingConfig, TestResult, PRResult, DetectedIssue } from "../types.js";

export async function createFixPR(
  analysis: AnalysisResult,
  testResult: TestResult | undefined,
  config: SelfHealingConfig,
): Promise<PRResult> {
  if (config.safety.dryRun) {
    return {
      number: 0,
      url: "",
      branch: generateBranchName(analysis.issue),
      title: analysis.prTitle,
      created: false,
    };
  }

  const { Octokit } = await import("octokit");
  const octokit = new Octokit({ auth: config.github.token });
  const { owner, repo, baseBranch } = config.github;

  const { data: ref } = await octokit.rest.git.getRef({ owner, repo, ref: `heads/${baseBranch}` });
  const baseSha = ref.object.sha;

  const branchName = generateBranchName(analysis.issue);
  await octokit.rest.git.createRef({ owner, repo, ref: `refs/heads/${branchName}`, sha: baseSha });

  const { data: baseCommit } = await octokit.rest.git.getCommit({ owner, repo, commit_sha: baseSha });

  const treeEntries = await Promise.all(
    analysis.fix
      .filter((p) => p.action !== "delete")
      .map(async (patch) => {
        const content = patch.content ?? "";
        const { data: blob } = await octokit.rest.git.createBlob({ owner, repo, content, encoding: "utf-8" });
        return { path: patch.filePath, mode: "100644" as const, type: "blob" as const, sha: blob.sha };
      }),
  );

  const deletions = analysis.fix
    .filter((p) => p.action === "delete")
    .map((patch) => ({
      path: patch.filePath,
      mode: "100644" as const,
      type: "blob" as const,
      sha: null as unknown as string,
    }));

  const { data: tree } = await octokit.rest.git.createTree({
    owner,
    repo,
    base_tree: baseCommit.tree.sha,
    tree: [...treeEntries, ...deletions],
  });

  const { data: commit } = await octokit.rest.git.createCommit({
    owner,
    repo,
    message: `${analysis.prTitle}\n\n${analysis.fixExplanation}\n\nGenerated by @emisso/self-healing`,
    tree: tree.sha,
    parents: [baseSha],
  });

  await octokit.rest.git.updateRef({ owner, repo, ref: `heads/${branchName}`, sha: commit.sha });

  const body = buildPRBody(analysis, testResult);
  const { data: pr } = await octokit.rest.pulls.create({
    owner,
    repo,
    title: analysis.prTitle,
    body,
    head: branchName,
    base: baseBranch,
  });

  return { number: pr.number, url: pr.html_url, branch: branchName, title: analysis.prTitle, created: true };
}

/** @internal Exported for testing */
export function generateBranchName(issue: DetectedIssue): string {
  const slug = issue.title
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .substring(0, 40)
    .replace(/-$/, "");
  const timestamp = Date.now().toString(36);
  return `self-healing/${slug}-${timestamp}`;
}

/** @internal Exported for testing */
export function buildPRBody(
  analysis: AnalysisResult,
  testResult: TestResult | undefined,
): string {
  let body = analysis.prBody;

  if (testResult) {
    body += "\n\n### Test Results\n";
    body += testResult.passed
      ? `\u2705 All tests passed (exit code ${testResult.exitCode})`
      : `\u274C Tests failed (exit code ${testResult.exitCode})`;
    body += `\n- Duration: ${(testResult.durationMs / 1000).toFixed(1)}s`;
    body += `\n- Sandbox: \`${testResult.sandboxId}\``;
    if (testResult.buildPassed !== undefined) {
      body += `\n- Build: ${testResult.buildPassed ? "\u2705" : "\u274C"}`;
    }
  }

  return body;
}
