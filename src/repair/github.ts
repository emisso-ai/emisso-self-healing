/**
 * GitHub PR Creator — Creates branches, commits patches, and opens PRs
 *
 * Uses Octokit to interact with the GitHub API.
 */

import type {
  AnalysisResult,
  DetectedIssue,
  PRResult,
  SelfHealingConfig,
  TestResult,
} from "../types";

/**
 * Create a GitHub PR with the AI-generated fix.
 *
 * Flow:
 *   1. Get the base branch SHA
 *   2. Create a new branch
 *   3. Commit the patches
 *   4. Create the PR
 */
export async function createFixPR(
  analysis: AnalysisResult,
  testResult: TestResult | undefined,
  config: SelfHealingConfig,
): Promise<PRResult> {
  if (config.safety.dryRun) {
    return {
      number: 0,
      url: "",
      branch: generateBranchName(analysis.issue),
      title: analysis.prTitle,
      created: false,
    };
  }

  // Dynamic import — keeps octokit optional
  const { Octokit } = await import("octokit");
  const octokit = new Octokit({ auth: config.github.token });
  const { owner, repo, baseBranch } = config.github;

  // 1. Get base branch SHA
  const { data: ref } = await octokit.rest.git.getRef({
    owner,
    repo,
    ref: `heads/${baseBranch}`,
  });
  const baseSha = ref.object.sha;

  // 2. Create branch
  const branchName = generateBranchName(analysis.issue);
  await octokit.rest.git.createRef({
    owner,
    repo,
    ref: `refs/heads/${branchName}`,
    sha: baseSha,
  });

  // 3. Create commits for each patch
  const { data: baseCommit } = await octokit.rest.git.getCommit({ owner, repo, commit_sha: baseSha });

  // Build tree entries for all patches
  const treeEntries = await Promise.all(
    analysis.fix
      .filter((p) => p.action !== "delete")
      .map(async (patch) => {
        const content = patch.content ?? "";
        const { data: blob } = await octokit.rest.git.createBlob({
          owner,
          repo,
          content,
          encoding: "utf-8",
        });
        return {
          path: patch.filePath,
          mode: "100644" as const,
          type: "blob" as const,
          sha: blob.sha,
        };
      }),
  );

  // Handle deletions
  const deletions = analysis.fix
    .filter((p) => p.action === "delete")
    .map((patch) => ({
      path: patch.filePath,
      mode: "100644" as const,
      type: "blob" as const,
      sha: null,
    }));

  // Create tree
  const { data: tree } = await octokit.rest.git.createTree({
    owner,
    repo,
    base_tree: baseCommit.tree.sha,
    tree: [...treeEntries, ...deletions] as Array<{
      path: string;
      mode: "100644";
      type: "blob";
      sha: string | null;
    }>,
  });

  // Create commit
  const { data: commit } = await octokit.rest.git.createCommit({
    owner,
    repo,
    message: `${analysis.prTitle}\n\n${analysis.fixExplanation}\n\nGenerated by @emisso/self-healing`,
    tree: tree.sha,
    parents: [baseSha],
  });

  // Update branch ref
  await octokit.rest.git.updateRef({
    owner,
    repo,
    ref: `heads/${branchName}`,
    sha: commit.sha,
  });

  // 4. Create PR
  const body = buildPRBody(analysis, testResult);
  const { data: pr } = await octokit.rest.pulls.create({
    owner,
    repo,
    title: analysis.prTitle,
    body,
    head: branchName,
    base: baseBranch,
  });

  return {
    number: pr.number,
    url: pr.html_url,
    branch: branchName,
    title: analysis.prTitle,
    created: true,
  };
}

// ---------------------------------------------------------------------------
// Helpers
// ---------------------------------------------------------------------------

function generateBranchName(issue: DetectedIssue): string {
  const slug = issue.title
    .toLowerCase()
    .replace(/[^a-z0-9]+/g, "-")
    .substring(0, 40)
    .replace(/-$/, "");
  const timestamp = Date.now().toString(36);
  return `self-healing/${slug}-${timestamp}`;
}

function buildPRBody(
  analysis: AnalysisResult,
  testResult?: TestResult,
): string {
  let body = analysis.prBody;

  if (testResult) {
    body += `\n\n### Test Results\n`;
    body += testResult.passed
      ? `✅ All tests passed (exit code ${testResult.exitCode})`
      : `❌ Tests failed (exit code ${testResult.exitCode})`;
    body += `\n- Duration: ${(testResult.durationMs / 1000).toFixed(1)}s`;
    body += `\n- Sandbox: \`${testResult.sandboxId}\``;
    if (testResult.buildPassed !== undefined) {
      body += `\n- Build: ${testResult.buildPassed ? "✅" : "❌"}`;
    }
  }

  return body;
}
